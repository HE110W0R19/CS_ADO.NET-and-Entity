DROP TABLE IF EXISTS WeekJournal;
DROP TABLE IF EXISTS Teacher;
DROP TABLE IF EXISTS AcademySubject;

CREATE TABLE AcademySubject
(
	Id int PRIMARY KEY AUTO_INCREMENT NOT NULL,
    SubjectName VARCHAR(50) NOT NULL
);

CREATE TABLE Teacher
(
	Id int PRIMARY KEY AUTO_INCREMENT,
    FirstName VARCHAR(32) NOT NULL,
    LastName VARCHAR(32) NOT NULL,
    Patronymic VARCHAR(32)
);

CREATE TABLE WeekJournal
(
    Id int PRIMARY KEY AUTO_INCREMENT,
	JournalTeacherId int NOT NULL,
    FOREIGN KEY (JournalTeacherId) REFERENCES Teacher(Id),
    JournalAcademSubId int NOT NULL,
    FOREIGN KEY (JournalAcademSubId) REFERENCES AcademySubject(Id),
    StartLessonDT DATETIME NOT NULL,
    EndLessonDT DATETIME NOT NULL
);

